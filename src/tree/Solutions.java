    public TreeNode solve(TreeNode root) {
        if (root == null) {
            return root;
        }
        TreeNode left = solve(root.left);
        TreeNode right = solve(root.right);

        root.left = right;
        root.right = left;
        return root;
    }

    /**
     * Q236: 二叉树的最近公共祖先
     * 给定一个二叉树, 找到该树中两个指定节点的最近公共祖先。
     * <p>
     * 百度百科中最近公共祖先的定义为：“对于有根树 T 的两个结点 p、q，最近公共祖先表示为一个结点 x，满足 x 是 p、q 的祖先且 x 的深度尽可能大（一个节点也可以是它自己的祖先）。”
     * <p>
     * 来源：力扣（LeetCode）
     * 链接：https://leetcode-cn.com/problems/lowest-common-ancestor-of-a-binary-tree
     * 著作权归领扣网络所有。商业转载请联系官方授权，非商业转载请注明出处。
     *
     * @param root
     * @param p
     * @param q
     * @return
     */
    public TreeNode lowestCommonAncestor(TreeNode root, TreeNode p, TreeNode q) {
        if (root == p || root == q || root == null) {
            return root;
        }
        TreeNode left = lowestCommonAncestor(root.left, p, q);
        TreeNode right = lowestCommonAncestor(root.right, p, q);

        if (left != null && right == null) {
            return left;
        } else if (left == null) {
            return right;
        } else {
            return root;
        }
    }

    /**
     * Q98:  验证二叉搜索树
     * 给定一个二叉树，判断其是否是一个有效的二叉搜索树。
     * <p>
     * 假设一个二叉搜索树具有如下特征：
     * <p>
     * 节点的左子树只包含小于当前节点的数。
     * 节点的右子树只包含大于当前节点的数。
     * 所有左子树和右子树自身必须也是二叉搜索树。
     * 按照二叉树中序遍历 left root right, 结果应该是递增的一个序列
     * 来源：力扣（LeetCode）
     * 链接：https://leetcode-cn.com/problems/validate-binary-search-tree
     * 著作权归领扣网络所有。商业转载请联系官方授权，非商业转载请注明出处。
     *
     * @param root
     * @return
     */
    public boolean isValidBST(TreeNode root) {
        if (root == null) {
            return true;
        }
        List<Integer> vals = new ArrayList<>();
        Deque<TreeNode> queue = new ArrayDeque<>();

        while (root != null || !queue.isEmpty()) {
            if (root != null) {
                queue.add(root);
                root = root.left;
            } else {
                root = queue.pollLast();
                vals.add(root.val);
                root = root.right;
            }
        }

        int a = vals.get(0);
        for (int i = 1; i < vals.size(); i++) {
            if (vals.get(i) <= a) {
                return false;
            }
            a = vals.get(i);
        }
        return true;
    }

    /**
     * Q450: 给定一个二叉搜索树的根节点 root 和一个值 key，删除二叉搜索树中的 key 对应的节点，并保证二叉搜索树的性质不变。
     * 返回二叉搜索树（有可能被更新）的根节点的引用。
     * <p>
     * 一般来说，删除节点可分为两个步骤：
     * <p>
     * 首先找到需要删除的节点；
     * 如果找到了，删除它。
     * 说明： 要求算法时间复杂度为 O(h)，h 为树的高度。
     * <p>
     * 来源：力扣（LeetCode）
     * 链接：https://leetcode-cn.com/problems/delete-node-in-a-bst
     * 著作权归领扣网络所有。商业转载请联系官方授权，非商业转载请注明出处。
     *
     * @param root
     * @param key
     * @return
     */
    public TreeNode deleteNode(TreeNode root, int key) {
        if (root == null) {
            return null;
        }

        if (key < root.val) {
            root.left = deleteNode(root.left, key);
            return root;
        }

        if (key > root.val) {
            root.right = deleteNode(root.right, key);
            return root;
        }

        if (root.left == null) {
            return root.right;
        }
        if (root.right == null) {
            return root.left;
        }

        TreeNode max = maxNode(root.left);
        TreeNode newRoot = new TreeNode(max.val);
        TreeNode newLeft = removeMax(root.left);
        newRoot.right = root.right;
        newRoot.left = newLeft;
        root.left = null;
        root.right = null;
        return newRoot;
    }

    private TreeNode maxNode(TreeNode root) {
        if (root.right == null) {
            return root;
        } else {
            return maxNode(root.right);
        }
    }

    private TreeNode removeMax(TreeNode node) {
        if (node.right == null) {
            return node.left;
        }
        node.right = removeMax(node.right);
        return node;
    }


