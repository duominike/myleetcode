    public TreeNode reBuildTree(int[] preOrder, int preStart, int preEnd, Map<Integer, Integer> map,
                                int inStart) {
        if (preEnd < preStart) {
            return null;
        }
        TreeNode root = new TreeNode(preOrder[preStart]);
        int rootIndex = map.get(root.val);
        int len = rootIndex - inStart;
        root.left = reBuildTree(preOrder, preStart + 1, preStart + len, map, inStart);
        root.right = reBuildTree(preOrder, preStart + 1 + len, preEnd, map, rootIndex + 1);
        return root;
    }


